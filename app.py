def bp_message(bp: str) -> str:
    s, d = parse_bp(bp)
    if s is None or d is None:
        return "혈압 미입력 또는 형식 미확인. 예: 118/76"
    if s >= 180 or d >= 120:
        return f"입력 혈압 {bp}: 매우 높은 혈압 범위일 수 있습니다. 즉시 의료적 평가가 필요할 수 있습니다."
    if s >= 140 or d >= 90:
        return f"입력 혈압 {bp}: 고혈압 범위. 승양·온보·강자극 전 혈압 상태 확인."
    return f"입력 혈압 {bp}: 입력 기준으로는 고혈압 경고가 자동 발생하지 않습니다."


