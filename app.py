def validate_uploaded_cases(df: pd.DataFrame) -> Dict[str, Any]:
    required = ["case_id", "text", "expected_safety", "expected_findings"]
    missing = [c for c in required if c not in df.columns]
    if missing:
        raise ValueError("필수 열 누락: " + ", ".join(missing))
    rows = []
    tp = fp = fn = 0
    safety_correct = 0
    for _, row in df.iterrows():
        record = {"chief_complaint": clean_text(row["text"])}
        triage = triage_state(record, [], [], False)
        pred_safety = triage["level"]
        expected_safety = clean_text(row["expected_safety"]).lower()
        safety_correct += int(pred_safety == expected_safety)
        predicted = {r["소견 후보"] for r in detect_findings(record)}
        expected = {x.strip() for x in clean_text(row["expected_findings"]).split("|") if x.strip()}
        tp_i, fp_i, fn_i = len(predicted & expected), len(predicted - expected), len(expected - predicted)
        tp += tp_i; fp += fp_i; fn += fn_i
        rows.append({"case_id": row["case_id"], "expected_safety": expected_safety, "predicted_safety": pred_safety,
                     "expected_findings": " | ".join(sorted(expected)), "predicted_findings": " | ".join(sorted(predicted)),
                     "finding_tp": tp_i, "finding_fp": fp_i, "finding_fn": fn_i})
    precision = tp / (tp + fp) if tp + fp else 0.0
    recall = tp / (tp + fn) if tp + fn else 0.0
    f1 = 2 * precision * recall / (precision + recall) if precision + recall else 0.0
    return {"rows": pd.DataFrame(rows), "safety_accuracy": safety_correct / len(df) if len(df) else 0.0,
            "precision": precision, "recall": recall, "f1": f1}


