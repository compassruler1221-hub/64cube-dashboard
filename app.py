def point_base_row(code: str) -> Dict[str, str]:
    m = re.match(r"([A-Z]+)(\d+)", code)
    if not m:
        ch, num = code, ""
    else:
        ch, num = m.group(1), m.group(2)
    kr, en = POINT_NAMES.get(code, (code, code))
    row = {
        "code": code,
        "혈명": kr,
        "standard_name": en,
        "경락": CHANNEL_KR.get(ch, ""),
        "부위군": infer_region(ch, int(num) if num else 0),
        "처방 방향축": default_axis(ch),
        "임상 의미": CHANNEL_MEANING.get(ch, ""),
        "뜸 가능 여부": "허한·냉감 조건이 명확할 때 한의사가 제한적으로 검토",
        "금기·주의": CHANNEL_CAUTION.get(ch, ""),
    }
    if code in CORE_POINT_EXTRA:
        row.update(CORE_POINT_EXTRA[code])
    return row

def infer_region(ch: str, num: int) -> str:
    if ch in ["LU", "LI", "PC", "TE", "HT", "SI"]:
        return "상지·흉부"
    if ch in ["ST", "SP", "LR", "GB", "KI"]:
        return "하지·복부"
    if ch == "BL":
        if num <= 30:
            return "배부·요부"
        return "하지·배부"
    if ch == "CV":
        return "복부·전면 정중"
    if ch == "DU":
        return "척추·두부 정중"
    return "기타"

def default_axis(ch: str) -> str:
    if ch in ["KI", "SP", "CV", "ST"]:
        return "보충축, 수렴·안정축"
    if ch in ["LR", "GB", "TE", "LI"]:
        return "소통·전환축"
    if ch in ["BL", "DU"]:
        return "보충축, 소통·전환축"
    if ch in ["HT", "PC"]:
        return "수렴·안정축"
    if ch in ["LU"]:
        return "보충축, 소통·전환축"
    return "참고"

def build_361_db() -> pd.DataFrame:
    rows = []
    for ch, count in CHANNEL_COUNTS.items():
        for i in range(1, count + 1):
            rows.append(point_base_row(f"{ch}{i}"))
    df = pd.DataFrame(rows)
    assert len(df) == 361
    return df.fillna("")

