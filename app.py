def v99_has_any(text: str, words: Sequence[str]) -> bool:
    normalized = clean_text(text)
    return any(word in normalized for word in words)


def v99_parse_bp(text: str) -> Tuple[int, int] | None:
    match = re.search(r"(\d{2,3})\s*/\s*(\d{2,3})", clean_text(text))
    return (int(match.group(1)), int(match.group(2))) if match else None


