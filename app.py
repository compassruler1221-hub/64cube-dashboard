def _add_segment_trace(fig: go.Figure, vertices: Sequence[Sequence[float]], edges: Sequence[Tuple[int, int]], name: str, width: float = 2.0, color: str = "#94a3b8", dash: str | None = None) -> None:
    x: List[float | None] = []
    y: List[float | None] = []
    z: List[float | None] = []
    for i, j in edges:
        x.extend([vertices[i][0], vertices[j][0], None])
        y.extend([vertices[i][1], vertices[j][1], None])
        z.extend([vertices[i][2], vertices[j][2], None])
    line: Dict[str, Any] = {"width": width, "color": color}
    if dash:
        line["dash"] = dash
    fig.add_trace(go.Scatter3d(x=x, y=y, z=z, mode="lines", line=line, name=name, hoverinfo="skip"))


