def topic_josa(word: str) -> str:
    """한글 받침 여부에 따라 은/는 선택"""
    if not word:
        return "은/는"
    last = word[-1]
    code = ord(last)
    if 0xAC00 <= code <= 0xD7A3:
        jong = (code - 0xAC00) % 28
        return "은" if jong else "는"
    return "은/는"

def safe_join(items, sep=", "):
    return sep.join([str(x) for x in items if str(x).strip()])

def contains_any(text: str, keys):
    text = (text or "").lower()
    return any(k.lower() in text for k in keys)

def parse_bp(bp_text: str):
    try:
        t = bp_text.replace(" ", "")
        if "/" not in t:
            return None
        s, d = t.split("/", 1)
        return int(s), int(d)
    except Exception:
        return None

def risk_level_label(score: int):
    if score >= 6:
        return "높음"
    if score >= 3:
        return "중간"
    if score >= 1:
        return "주의"
    return "낮음"

