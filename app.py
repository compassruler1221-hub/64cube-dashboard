def acupoint_search_df(query: str, filters_meridian: List[str], filters_axis: List[str]) -> pd.DataFrame:
    df = make_df(ACUPOINT_361)
    if filters_meridian:
        df = df[df["경락"].isin(filters_meridian)]
    if filters_axis:
        mask = pd.Series([False] * len(df), index=df.index)
        for ax in filters_axis:
            mask = mask | df["처방 방향축"].str.contains(re.escape(ax), na=False)
        df = df[mask]
    if query:
        q = query.lower()
        cols = ["code", "혈명", "standard_name", "경락", "부위군", "처방 방향축", "임상 의미", "주의점"]
        mask = pd.Series([False] * len(df), index=df.index)
        for col in cols:
            mask = mask | df[col].astype(str).str.lower().str.contains(q, regex=False, na=False)
        df = df[mask]
    return df


