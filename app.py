def n_to_hyo_bits(n: int) -> str:
    return "".join(str((n >> i) & 1) for i in range(6))

def n_to_codon(n: int) -> str:
    bits = n_to_hyo_bits(n)
    return "".join(
        BITS_TO_BASE[bits[i:i+2]]
        for i in [0, 2, 4]
    )

def codon_to_n(codon: str) -> int:
    bits = "".join(BASE_TO_BITS[b] for b in codon.upper().replace("T", "U"))
    return sum(int(bits[i]) << i for i in range(6))

def n_to_aa(n: int) -> str:
    return GENETIC_CODE[n_to_codon(n)]

def q6_neighbors(n: int) -> List[int]:
    return [n ^ (1 << i) for i in range(6)]

def h34_neighbors(n: int) -> List[int]:
    codon = n_to_codon(n)
    bases = ["G", "A", "U", "C"]
    out = []
    for pos in range(3):
        for b in bases:
            if b != codon[pos]:
                c = codon[:pos] + b + codon[pos+1:]
                out.append(codon_to_n(c))
    return sorted(set(out))

def build_q6_reference_df() -> pd.DataFrame:
    rows = []
    for n in [0, 9, 18, 63]:
        rows.append({
            "n": n,
            "효 순서 비트": n_to_hyo_bits(n),
            "codon": n_to_codon(n),
            "amino_acid": n_to_aa(n),
            "Q6 이웃": ", ".join(map(str, q6_neighbors(n))),
            "H(3,4) 이웃 수": len(h34_neighbors(n)),
        })
    return pd.DataFrame(rows)

