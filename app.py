def moxa_point_rows(formula: Dict[str, Any]) -> List[Dict[str, str]]:
    moxa = formula["moxa"]
    rows = []
    for code in formula["core_points"]:
        pt = acupoint_by_code(code)
        if code in ["CV8"]:
            status = "보류"
            strength = "보류"
        elif code.startswith("CV") or code in ["BL23", "CV4", "CV6", "ST36", "GV4"]:
            status = "조건부 가능"
            strength = "약~중"
        elif code in ["KI3", "KI6", "SP6", "HT7", "PC6"]:
            status = "조건부 가능"
            strength = "약"
        else:
            status = "제한적"
            strength = "약"
        rows.append({
            "code": code,
            "혈명": pt["혈명"],
            "가능/보류": status,
            "주의 조건": moxa["caution"],
            "권장 강도": strength,
            "시술 전 확인": pt["주의점"],
        })
    return rows


