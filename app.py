def clean_cell(x: Any) -> Any:
    if x is None:
        return ""
    try:
        if isinstance(x, float) and pd.isna(x):
            return ""
    except Exception:
        pass
    if isinstance(x, list):
        return ", ".join(str(i) for i in x if i is not None)
    return x


def clean_rows(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    return [{k: clean_cell(v) for k, v in row.items()} for row in rows]


def make_df(rows: List[Dict[str, Any]]) -> pd.DataFrame:
    return pd.DataFrame(clean_rows(rows)).fillna("")


def show_df(rows_or_df: Any, height: Optional[int] = None, hide_index: bool = True) -> None:
    if isinstance(rows_or_df, pd.DataFrame):
        df = rows_or_df.fillna("")
    else:
        df = make_df(rows_or_df)

    # StreamlitInvalidHeightError 방지:
    # height는 문자열 "auto"가 아니라 정수만 넘긴다.
    if height is None:
        st.dataframe(df, use_container_width=True, hide_index=hide_index)
    else:
        st.dataframe(df, use_container_width=True, hide_index=hide_index, height=int(height))


