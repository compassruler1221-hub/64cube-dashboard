def make_acupoint_db():
    rows = []
    for prefix, mer_kr, count, region, axes, direction, caution in MERIDIANS:
        for i in range(1, count + 1):
            code = f"{prefix}{i}"
            if code in SPECIAL_POINTS:
                name, std, meridian, channel, axes_str, clinical, moxa, caution2 = SPECIAL_POINTS[code]
                rows.append({
                    "code": code, "경혈명": name, "standard_name": std, "경락": channel, "부위군": region,
                    "처방 방향축": axes_str, "임상 방향": clinical, "뜸 가능 여부": moxa, "금기·주의": caution2,
                    "기본후보제외": code in SENSITIVE_DEFAULT_EXCLUDE,
                })
            else:
                rows.append({
                    "code": code, "경혈명": code, "standard_name": code, "경락": mer_kr, "부위군": region,
                    "처방 방향축": safe_join(axes), "임상 방향": direction, "뜸 가능 여부": "허한·냉감 조건이 명확할 때 한의사가 제한적으로 검토",
                    "금기·주의": caution, "기본후보제외": code in SENSITIVE_DEFAULT_EXCLUDE,
                })
    df = pd.DataFrame(rows)
    df["한의 임상 요약"] = df["code"] + " " + df["경혈명"] + " — " + df["임상 방향"]
    return df

