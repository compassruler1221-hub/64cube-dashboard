def parse_bp(bp: str) -> Tuple[int, int] | None:
    nums = re.findall(r"\d+", bp or "")
    if len(nums) >= 2:
        return int(nums[0]), int(nums[1])
    return None

def bp_message(bp: str) -> Tuple[str, str]:
    parsed = parse_bp(bp)
    if not parsed:
        return "혈압 미입력. 고혈압·저혈압 병력은 별도 확인.", "info"
    sys, dia = parsed
    if sys >= 160 or dia >= 100:
        return f"입력 혈압 {bp}: 높음. 승양·온보·강자극 전 재확인 필요.", "error"
    if sys >= 140 or dia >= 90:
        return f"입력 혈압 {bp}: 고혈압 범위 가능. 혈압 재측정 및 병용약 확인.", "warning"
    if sys < 90 or dia < 60:
        return f"입력 혈압 {bp}: 저혈압 가능. 어지럼·기립성 증상 확인.", "warning"
    return f"입력 혈압 {bp}: 입력 기준으로는 고혈압 경고가 자동 발생하지 않음.", "success"

