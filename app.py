def clean_text(value: Any) -> str:
    if value is None:
        return ""
    try:
        if pd.isna(value):
            return ""
    except Exception:
        pass
    text = str(value).strip()
    return "" if text.lower() in {"none", "nan", "null", "미기록", "0"} else text


def split_sentences(text: str) -> List[str]:
    text = re.sub(r"\s+", " ", clean_text(text))
    return [s.strip() for s in re.split(r"(?<=[.!?。])\s+|[\n;]+", text) if s.strip()]


def is_nonpatient_context(sentence: str) -> bool:
    return any(x in sentence for x in ["교육자료", "연구용", "테스트용", "템플릿", "가족력", "아버지", "어머니", "문헌에 따르면"])


def term_negated(sentence: str, term: str) -> bool:
    if is_nonpatient_context(sentence):
        return True
    idx = sentence.find(term)
    if idx < 0:
        return False
    window = sentence[max(0, idx - 16): min(len(sentence), idx + len(term) + 18)]
    neg = ["없음", "없다", "없고", "없으며", "없었다", "아니다", "아니며", "부인", "호소하지 않", "관찰되지 않", "현재는 없"]
    if any(n in window for n in neg):
        return True
    if "과거" in sentence and any(n in sentence for n in ["현재는 없", "오늘은 없", "지금은 없"]):
        return True
    return False


def active_term(sentence: str, terms: Sequence[str]) -> str | None:
    for term in terms:
        if term in sentence and not term_negated(sentence, term):
            return term
    return None


def record_text_fields(record: Mapping[str, Any]) -> Dict[str, str]:
    fields = [
        "chief_complaint", "onset", "course", "site_quality", "aggravating", "relieving", "associated",
        "cold_heat", "sweat", "thirst", "appetite_digestion", "stool", "urine", "sleep", "emotion",
        "pain", "tongue", "pulse_tcm", "abdomen", "history", "medications", "allergy", "notes",
    ]
    return {field: clean_text(record.get(field, "")) for field in fields}


def combined_record_text(record: Mapping[str, Any]) -> str:
    return "\n".join(v for v in record_text_fields(record).values() if v)


