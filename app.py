def safe_text(x: Any) -> str:
    """None/NaN/빈칸이 화면에 그대로 보이지 않도록 정리."""
    if x is None:
        return ""
    s = str(x)
    if s.lower() in ["none", "nan", "nat"]:
        return ""
    return s.strip()

def clean_df(rows: List[Dict[str, Any]]) -> pd.DataFrame:
    """None 행/None 셀 제거용 DataFrame 생성."""
    clean_rows = []
    for r in rows:
        nr = {safe_text(k): safe_text(v) for k, v in r.items()}
        if any(v for v in nr.values()):
            clean_rows.append(nr)
    return pd.DataFrame(clean_rows).fillna("")

def show_df(rows_or_df, height: int | None = None, use_container_width: bool = True):
    if isinstance(rows_or_df, pd.DataFrame):
        df = rows_or_df.fillna("")
    else:
        df = clean_df(rows_or_df)
    if df.empty:
        st.info("표시할 항목이 없습니다.")
    else:
        st.dataframe(df, use_container_width=use_container_width, hide_index=True, height=height)

def box(text: str, kind: str = "info"):
    text = safe_text(text)
    if not text:
        return
    if kind == "warning":
        st.warning(text)
    elif kind == "error":
        st.error(text)
    elif kind == "success":
        st.success(text)
    else:
        st.info(text)

def bullet(items: List[str]):
    for item in items:
        if safe_text(item):
            st.markdown(f"- {item}")

def join_nonempty(items: List[str], sep: str = ", ") -> str:
    return sep.join([safe_text(x) for x in items if safe_text(x)])


